/**
 * The Log Client response
 * @export
 * @interface LogClient
 */
export interface LogClient {
  /**
   * The unique identifier of the client
   * @type {string}
   * @memberof LogClient
   */
  id?: string | null;
  /**
   * The name of the client
   * @type {string}
   * @memberof LogClient
   */
  name?: string | null;
}
